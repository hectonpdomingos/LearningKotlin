fun main(args: Array<String>) {

    var counter = 0

    //Do While Loop

    do{
        println("Counting ... $counter")
        counter++
    }while(counter < 5)

    println("The end of the Do While Loop")
    // While loop

    while (counter < 5){
        println("Counting ... $counter")
        counter++
    }
    println("The end of the While Loop")
}